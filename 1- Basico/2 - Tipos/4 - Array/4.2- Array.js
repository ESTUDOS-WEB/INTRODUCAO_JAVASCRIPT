let array1 = new Array();

/* Adiociona na posição 0 */
array1[0] = 'Item 1';
console.log(array1);

/* Adiciona no fim */
array1.push('Item 2');
console.log(array1);

/* Adiciona no comeco */
array1.unshift('Item 3');
console.log(array1);

/* Remover no comeco */
array1.shift();
console.log(array1);

/* Remover no final */
array1.pop();
console.log(array1);
